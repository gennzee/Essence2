package model;

/**
 *
 * @author tanks
 */
public class Invoice {
    private int id;
    private int total;
    private String date;
    private String name;
    private String phone;

    public Invoice() {
    }

    public Invoice(int id, int total, String date, String name, String phone) {
        this.id = id;
        this.total = total;
        this.date = date;
        this.name = name;
        this.phone = phone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }


    
}
